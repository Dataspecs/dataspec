use std::collections::HashMap;
use std::fmt::Write;

use crate::entities::{
    Column, Config, Entity, Model, Operation, OperationUsage, Template, TemplateUsage, Test,
    Transformation,
};
use crate::error::Result;

use super::ident::entity_name_to_ident;

struct Partitioned {
    models: Vec<(String, Model)>,
    transformations: Vec<(String, Transformation)>,
    templates: Vec<(String, Template)>,
    tests: Vec<(String, Test)>,
    operations: Vec<(String, Operation)>,
}

pub fn emit_catalog_rs(entities: &[(std::path::PathBuf, Entity)], config: &Config) -> Result<String> {
    let mut partitioned = Partitioned {
        models: Vec::new(),
        transformations: Vec::new(),
        templates: Vec::new(),
        tests: Vec::new(),
        operations: Vec::new(),
    };

    for (_, entity) in entities {
        let name = entity.name();
        let ident = entity_name_to_ident(name)?;
        match entity {
            Entity::Config(_) => {}
            Entity::Model(m) => partitioned.models.push((ident, m.clone())),
            Entity::Transformation(t) => partitioned.transformations.push((ident, t.clone())),
            Entity::Template(t) => partitioned.templates.push((ident, t.clone())),
            Entity::Test(t) => partitioned.tests.push((ident, t.clone())),
            Entity::Operation(o) => partitioned.operations.push((ident, o.clone())),
        }
    }

    let mut out = String::from("// @generated by dataspec spec_builder; do not edit\n\n");
    writeln!(
        out,
        "use dataspec::{{Config, DataCatalog, Model, Operation, Template, Test, Transformation}};"
    )
    .unwrap();
    writeln!(out, "use std::collections::HashMap;").unwrap();
    writeln!(out, "use std::sync::LazyLock;\n").unwrap();

    writeln!(
        out,
        "static CONFIG: LazyLock<Config> = LazyLock::new(|| {});",
        emit_config_literal(config)
    )
    .unwrap();
    writeln!(out).unwrap();

    for (ident, model) in &partitioned.models {
        emit_lazy_static(&mut out, ident, "Model", &emit_model_literal(model));
    }
    for (ident, t) in &partitioned.transformations {
        emit_lazy_static(&mut out, ident, "Transformation", &emit_transformation_literal(t));
    }
    for (ident, t) in &partitioned.templates {
        emit_lazy_static(&mut out, ident, "Template", &emit_template_literal(t));
    }
    for (ident, t) in &partitioned.tests {
        emit_lazy_static(&mut out, ident, "Test", &emit_test_literal(t));
    }
    for (ident, o) in &partitioned.operations {
        emit_lazy_static(&mut out, ident, "Operation", &emit_operation_literal(o));
    }

    let model_idents: Vec<_> = partitioned.models.iter().map(|(i, _)| i.as_str()).collect();
    let transformation_idents: Vec<_> = partitioned
        .transformations
        .iter()
        .map(|(i, _)| i.as_str())
        .collect();
    let template_idents: Vec<_> = partitioned.templates.iter().map(|(i, _)| i.as_str()).collect();
    let test_idents: Vec<_> = partitioned.tests.iter().map(|(i, _)| i.as_str()).collect();
    let operation_idents: Vec<_> = partitioned.operations.iter().map(|(i, _)| i.as_str()).collect();

    emit_accessor_fn(&mut out, "tmpls", "Template", &template_idents);
    emit_accessor_fn(&mut out, "models", "Model", &model_idents);
    emit_accessor_fn(&mut out, "transformations", "Transformation", &transformation_idents);
    emit_accessor_fn(&mut out, "tests", "Test", &test_idents);
    emit_accessor_fn(&mut out, "operations", "Operation", &operation_idents);

    writeln!(out, "pub fn register_data() -> DataCatalog {{").unwrap();
    writeln!(out, "    let mut data_catalog = DataCatalog::new();").unwrap();
    emit_register_line(&mut out, "register_models", &model_idents);
    emit_register_line(&mut out, "register_transformations", &transformation_idents);
    emit_register_line(&mut out, "register_tests", &test_idents);
    emit_register_line(&mut out, "register_operations", &operation_idents);
    emit_register_line(&mut out, "register_templates", &template_idents);
    writeln!(out, "    data_catalog.register_config((*CONFIG).clone());").unwrap();
    writeln!(out, "    data_catalog").unwrap();
    writeln!(out, "}}").unwrap();

    Ok(out)
}

fn emit_lazy_static(out: &mut String, ident: &str, ty: &str, literal: &str) {
    writeln!(
        out,
        "pub static {ident}: LazyLock<{ty}> = LazyLock::new(|| {literal});"
    )
    .unwrap();
    writeln!(out).unwrap();
}

fn emit_accessor_fn(out: &mut String, name: &str, ty: &str, idents: &[&str]) {
    writeln!(out, "pub fn {name}() -> Vec<&'static {ty}> {{").unwrap();
    if idents.is_empty() {
        writeln!(out, "    vec![]").unwrap();
    } else {
        writeln!(
            out,
            "    vec![{}]",
            idents
                .iter()
                .map(|i| format!("&{i}"))
                .collect::<Vec<_>>()
                .join(", ")
        )
        .unwrap();
    }
    writeln!(out, "}}\n").unwrap();
}

fn emit_register_line(out: &mut String, method: &str, idents: &[&str]) {
    if idents.is_empty() {
        writeln!(out, "    data_catalog.{method}(vec![]);").unwrap();
    } else {
        writeln!(
            out,
            "    data_catalog.{method}(vec![{}]);",
            idents
                .iter()
                .map(|i| format!("&{i}"))
                .collect::<Vec<_>>()
                .join(", ")
        )
        .unwrap();
    }
}

fn emit_config_literal(config: &Config) -> String {
    format!("Config {{ props: {} }}", emit_hashmap(&config.props))
}

fn emit_model_literal(model: &Model) -> String {
    format!(
        "Model {{ name: {}, description: {}, tags: {}, table_id: {}, managed: {}, disabled: {}, meta: {}, default_transformation: {} }}",
        rust_string_expr(&model.name),
        emit_option_string(&model.description),
        emit_option_vec_string(&model.tags),
        emit_option_string(&model.table_id),
        model.managed,
        model.disabled,
        emit_option_hashmap(&model.meta),
        emit_option_string(&model.default_transformation),
    )
}

fn emit_transformation_literal(t: &Transformation) -> String {
    format!(
        "Transformation {{ name: {}, sql_code: {}, model: {}, dependent_tables: {}, used_variables: {}, template: {}, columns: {}, tests: {}, pre_runs: {}, post_runs: {}, init_runs: {} }}",
        rust_string_expr(&t.name),
        rust_string_expr(&t.sql_code),
        rust_string_expr(&t.model),
        emit_vec_string(&t.dependent_tables),
        emit_option_vec_string(&t.used_variables),
        emit_option_template_usage(&t.template),
        emit_option_columns(&t.columns),
        emit_option_vec_string(&t.tests),
        emit_option_operation_usages(&t.pre_runs),
        emit_option_operation_usages(&t.post_runs),
        emit_option_operation_usages(&t.init_runs),
    )
}

fn emit_template_literal(t: &Template) -> String {
    format!(
        "Template {{ name: {}, description: {}, sql_code: {}, dependent_tables: {}, used_variables: {}, template: {} }}",
        rust_string_expr(&t.name),
        emit_option_string(&t.description),
        rust_string_expr(&t.sql_code),
        emit_vec_string(&t.dependent_tables),
        emit_option_vec_string(&t.used_variables),
        emit_option_template_usage(&t.template),
    )
}

fn emit_test_literal(t: &Test) -> String {
    format!(
        "Test {{ name: {}, description: {}, sql_code: {}, dependent_tables: {}, used_variables: {} }}",
        rust_string_expr(&t.name),
        emit_option_string(&t.description),
        rust_string_expr(&t.sql_code),
        emit_vec_string(&t.dependent_tables),
        emit_option_vec_string(&t.used_variables),
    )
}

fn emit_operation_literal(o: &Operation) -> String {
    format!(
        "Operation {{ name: {}, description: {}, tags: {}, sql_code: {}, template: {}, dependent_tables: {}, used_variables: {} }}",
        rust_string_expr(&o.name),
        emit_option_string(&o.description),
        emit_option_vec_string(&o.tags),
        rust_string_expr(&o.sql_code),
        emit_option_template_usage(&o.template),
        emit_vec_string(&o.dependent_tables),
        emit_option_vec_string(&o.used_variables),
    )
}

fn emit_column(col: &Column) -> String {
    format!(
        "Column {{ name: {}, description: {}, data_type: {}, labels: {}, tests: {} }}",
        rust_string_expr(&col.name),
        emit_option_string(&col.description),
        emit_option_string(&col.data_type),
        emit_option_vec_string(&col.labels),
        emit_option_vec_string(&col.tests),
    )
}

fn emit_template_usage(usage: &TemplateUsage) -> String {
    format!(
        "TemplateUsage {{ name: {}, props: {} }}",
        rust_string_expr(&usage.name),
        emit_option_hashmap(&usage.props),
    )
}

fn emit_operation_usage(usage: &OperationUsage) -> String {
    format!(
        "OperationUsage {{ name: {}, props: {} }}",
        rust_string_expr(&usage.name),
        emit_option_hashmap(&usage.props),
    )
}

fn emit_option_columns(cols: &Option<Vec<Column>>) -> String {
    match cols {
        Some(cols) => format!(
            "Some(vec![{}])",
            cols.iter().map(emit_column).collect::<Vec<_>>().join(", ")
        ),
        None => "None".to_string(),
    }
}

fn emit_option_template_usage(value: &Option<TemplateUsage>) -> String {
    match value {
        Some(usage) => format!("Some({})", emit_template_usage(usage)),
        None => "None".to_string(),
    }
}

fn emit_option_operation_usages(value: &Option<Vec<OperationUsage>>) -> String {
    match value {
        Some(usages) => format!(
            "Some(vec![{}])",
            usages
                .iter()
                .map(emit_operation_usage)
                .collect::<Vec<_>>()
                .join(", ")
        ),
        None => "None".to_string(),
    }
}

fn emit_option_string(value: &Option<String>) -> String {
    match value {
        Some(s) => format!("Some({})", rust_string_expr(s)),
        None => "None".to_string(),
    }
}

fn emit_option_vec_string(value: &Option<Vec<String>>) -> String {
    match value {
        Some(v) => format!("Some({})", emit_vec_string(v)),
        None => "None".to_string(),
    }
}

fn emit_vec_string(values: &[String]) -> String {
    if values.is_empty() {
        "vec![]".to_string()
    } else {
        format!(
            "vec![{}]",
            values
                .iter()
                .map(|s| rust_string_expr(s))
                .collect::<Vec<_>>()
                .join(", ")
        )
    }
}

fn emit_hashmap(map: &HashMap<String, String>) -> String {
    if map.is_empty() {
        "HashMap::new()".to_string()
    } else {
        format!(
            "HashMap::from([{}])",
            map.iter()
                .map(|(k, v)| format!("({}, {})", rust_string_expr(k), rust_string_expr(v)))
                .collect::<Vec<_>>()
                .join(", ")
        )
    }
}

fn emit_option_hashmap(value: &Option<HashMap<String, String>>) -> String {
    match value {
        Some(map) => format!("Some({})", emit_hashmap(map)),
        None => "None".to_string(),
    }
}

fn rust_string_literal(s: &str) -> String {
    let mut out = String::from('"');
    for c in s.chars() {
        match c {
            '\\' => out.push_str("\\\\"),
            '"' => out.push_str("\\\""),
            '\n' => out.push_str("\\n"),
            '\r' => out.push_str("\\r"),
            '\t' => out.push_str("\\t"),
            c => out.push(c),
        }
    }
    out.push('"');
    out
}

fn rust_string_expr(s: &str) -> String {
    format!("{}.to_string()", rust_string_literal(s))
}
