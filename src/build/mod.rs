use std::collections::{HashMap, HashSet};
use std::fs;
use std::path::{Path, PathBuf};

use crate::entities::{Config, Entity};
use crate::error::{ParseError, Result};
use crate::parser::parse_spec_dir;

/// Parse all `.md` files under `specs_dir` and write generated Rust to `output_path`.
pub fn spec_builder(specs_dir: impl AsRef<Path>, output_path: impl AsRef<Path>) -> Result<()> {
    let specs_dir = specs_dir.as_ref();
    let output_path = output_path.as_ref();
    let entities = parse_spec_dir(specs_dir)?;
    validate_entities(&entities)?;

    let config = extract_config(&entities);
    let catalog_entities: Vec<_> = entities
        .iter()
        .filter(|(_, e)| !matches!(e, Entity::Config(_)))
        .cloned()
        .collect();

    let source = emit::emit_catalog_rs(&catalog_entities, &config)?;

    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent).map_err(|source| ParseError::Io {
            path: parent.to_path_buf(),
            source,
        })?;
    }
    fs::write(output_path, &source).map_err(|source| ParseError::Io {
        path: output_path.to_path_buf(),
        source,
    })?;

    println!("cargo:rerun-if-changed={}", specs_dir.display());
    for (path, _) in &entities {
        println!("cargo:rerun-if-changed={}", path.display());
    }

    Ok(())
}

fn extract_config(entities: &[(PathBuf, Entity)]) -> Config {
    entities
        .iter()
        .find_map(|(_, e)| match e {
            Entity::Config(c) => Some(c.clone()),
            _ => None,
        })
        .unwrap_or_default()
}

fn validate_entities(entities: &[(PathBuf, Entity)]) -> Result<()> {
    let mut config_count = 0usize;
    let mut seen: HashMap<&'static str, HashSet<String>> = HashMap::new();

    for (_, entity) in entities {
        if matches!(entity, Entity::Config(_)) {
            config_count += 1;
            if config_count > 1 {
                return Err(ParseError::MultipleConfig);
            }
            continue;
        }

        let kind = entity.kind();
        let name = entity.name().to_string();
        let names = seen.entry(kind).or_default();
        if !names.insert(name.clone()) {
            return Err(ParseError::DuplicateEntity {
                kind: kind.to_string(),
                name,
            });
        }
    }

    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::entities::DataCatalog;
    use crate::parser::parse_spec_dir;

    fn fixture_dir() -> PathBuf {
        PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../specs/data-specs")
    }

    #[test]
    fn spec_builder_generates_expected_output() {
        let dir = tempfile::tempdir().unwrap();
        let out = dir.path().join("catalog.rs");
        spec_builder(fixture_dir(), &out).unwrap();

        let source = fs::read_to_string(&out).unwrap();
        assert!(source.contains("@generated by dataspec spec_builder"));
        assert!(source.contains("pub fn register_data() -> DataCatalog"));
        assert!(source.contains("pub static dummy_model: LazyLock<Model>"));
        assert!(source.contains(
            "pub static dummy_model__default_transformation: LazyLock<Transformation>"
        ));
        assert!(source.contains("pub static dummy_template: LazyLock<Template>"));
        assert!(source.contains("pub static dummy_operation: LazyLock<Operation>"));
    }

    #[test]
    fn rejects_multiple_config_files() {
        let dir = tempfile::tempdir().unwrap();
        let specs = dir.path().join("specs");
        fs::create_dir_all(&specs).unwrap();
        fs::write(specs.join("config1.md"), "# config1\n\n## Type\n\nconfig\n").unwrap();
        fs::write(specs.join("config2.md"), "# config2\n\n## Type\n\nconfig\n").unwrap();

        let err = spec_builder(&specs, dir.path().join("catalog.rs")).unwrap_err();
        assert!(matches!(err, ParseError::MultipleConfig));
    }

    #[test]
    fn rejects_duplicate_model_names() {
        let dir = tempfile::tempdir().unwrap();
        let specs = dir.path().join("specs");
        fs::create_dir_all(&specs).unwrap();
        let model_md = "# dup_model\n\n## Type\n\nmodel\n";
        fs::write(specs.join("a.md"), model_md).unwrap();
        fs::write(specs.join("b.md"), model_md).unwrap();

        let err = spec_builder(&specs, dir.path().join("catalog.rs")).unwrap_err();
        assert!(matches!(
            err,
            ParseError::DuplicateEntity { kind, name }
            if kind == "model" && name == "dup_model"
        ));
    }

    #[test]
    fn generated_catalog_registers_entities() {
        let entities = parse_spec_dir(fixture_dir()).unwrap();
        let catalog = register_parsed_entities(&entities);
        assert!(catalog.models_by_name.contains_key("dummy_model"));
        assert!(catalog
            .transformations_by_name
            .contains_key("dummy_model__default_transformation"));
        assert!(catalog.templates_by_name.contains_key("dummy_template"));
        assert!(catalog.operations_by_name.contains_key("dummy_operation"));
        assert_eq!(catalog.config.get("environment"), Some("development"));
    }

    fn register_parsed_entities(entities: &[(PathBuf, Entity)]) -> DataCatalog {
        let mut catalog = DataCatalog::new();
        for (_, entity) in entities {
            match entity {
                Entity::Config(c) => catalog.register_config(c.clone()),
                Entity::Model(m) => {
                    let leaked: &'static crate::Model = Box::leak(Box::new(m.clone()));
                    catalog.register_model(leaked);
                }
                Entity::Transformation(t) => {
                    let leaked: &'static crate::Transformation = Box::leak(Box::new(t.clone()));
                    catalog.register_transformation(leaked);
                }
                Entity::Template(t) => {
                    let leaked: &'static crate::Template = Box::leak(Box::new(t.clone()));
                    catalog.register_template(leaked);
                }
                Entity::Test(t) => {
                    let leaked: &'static crate::Test = Box::leak(Box::new(t.clone()));
                    catalog.register_test(leaked);
                }
                Entity::Operation(o) => {
                    let leaked: &'static crate::Operation = Box::leak(Box::new(o.clone()));
                    catalog.register_operation(leaked);
                }
            }
        }
        catalog
    }
}
